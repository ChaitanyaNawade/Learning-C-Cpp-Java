// Program to demonstrate array declaration and initialization using a member initialization list in Java

class Array2
{
    public static void main(String A[])
    {
        int Arr[] = {10,20,30,40,50}; //Member initialization list


        System.out.println(Arr[0]);
        System.out.println(Arr[1]);
        System.out.println(Arr[2]);
        System.out.println(Arr[3]);
        System.out.println(Arr[4]);
    }
}
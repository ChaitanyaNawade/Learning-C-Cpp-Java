// Program to demonstrate the creation, initialization, and display of a one-dimensional array in Java

class Array1
{
    public static void main(String A[])
    {
        int Arr[] = new int[5];

        Arr[0] = 10;
        Arr[1] = 20;
        Arr[2] = 30;
        Arr[3] = 40;
        Arr[4] = 50;

        System.out.println(Arr[0]);
        System.out.println(Arr[1]);
        System.out.println(Arr[2]);
        System.out.println(Arr[3]);
        System.out.println(Arr[4]);
    }
}